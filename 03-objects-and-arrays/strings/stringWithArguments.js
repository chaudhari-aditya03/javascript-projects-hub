// String with Arguments Example
function greetUser(userName, course) {
    return `Hello, ${userName}! Welcome to the ${course} course.`;
}
console.log(greetUser("Aditya", "JavaScript"));

// Output: "Hello, Aditya! Welcome to the JavaScript course."